# ── formatting-edge / recorded-behavior checks ────────────────────────────────

def test_device_card_rounds_wl_to_integer_microns():
    """RECORDED BEHAVIOR (not a bug to fix): W/L use ``:.0f`` formatting, so
    fractional/sub-µm sizes are silently rounded. A caller passing SI metres
    (e.g. 1e-15) would emit ``W=0``. The solvers use integer-µm W/L, so this is
    safe in practice, but the rounding is pinned here so it can't drift unnoticed.
    """
    tiny = dict(_SIZES)
    tiny["M6"] = (1e-15, 2e-15)      # sub-µm -> rounds to 0
    tiny["M7"] = (100.7, 50.4)       # fractional µm -> nearest integer
    scs = gen_amp_netlist(tiny, _BIAS)
    assert re.search(r"^M6 \(VDD NET2 VB\) pmos_TFT W=0 L=0 NF=1 ", scs, re.M)
    assert re.search(r"^M7 \(NET2 VOP vip\) pmos_TFT W=101 L=50 NF=1 ", scs, re.M)


def test_amp_and_chopper_netlists_are_strings_without_trailing_none():
    # Both generators return a single joined string (no None sentinels leaked in).
    amp = gen_amp_netlist(_SIZES, _BIAS)
    chop = gen_chopper_netlist(_SIZES, _BIAS)
    assert isinstance(amp, str) and isinstance(chop, str)
    assert "None" not in amp and "None" not in chop
