#[allow(clippy::too_many_arguments)]
pub unsafe fn dc(
    device: *mut CoBsim4,
    terminals: *const f64,
    currents: *mut f64,
    conductance: *mut f64,
    charges: *mut f64,
    capacitance: *mut f64,
    op: *mut f64,
) -> c_int {
    let _guard = lock();
    dc_inner(
        device,
        terminals,
        currents,
        conductance,
        charges,
        capacitance,
        op,
    )
}

#[allow(clippy::too_many_arguments)]
pub unsafe fn eval(
    device: *mut CoBsim4,
    terminals: *const f64,
    currents: *mut f64,
    conductance: *mut f64,
    charges: *mut f64,
    capacitance: *mut f64,
    op: *mut f64,
) -> c_int {
    let _guard = lock();
    let status = dc_inner(
        device,
        terminals,
        currents,
        conductance,
        charges,
        capacitance,
        op,
    );
    if status != OK {
        return status;
    }
    enforce_terminal_conservation(currents, conductance, charges, capacitance)
}

/// All-`void*` evaluation ABI (host.c `co_bsim4_eval_vp`), for the Numba bridge.
pub unsafe fn eval_vp(
    device: *mut CoBsim4,
    terminals: *const f64,
    currents: *mut f64,
    conductance: *mut f64,
    charges: *mut f64,
    capacitance: *mut f64,
) -> c_int {
    let mut op = [0.0f64; 8];
    eval(
        device,
        terminals,
        currents,
        conductance,
        charges,
        capacitance,
        op.as_mut_ptr(),
    )
}

pub unsafe fn noise(
    device: *mut CoBsim4,
    frequency_hz: f64,
    total_real: *mut f64,
    total_imag: *mut f64,
    flicker_real: *mut f64,
    flicker_imag: *mut f64,
) -> c_int {
    // `!(f > 0.0)` (not the "cleaner" `f <= 0.0`) is deliberate host.c parity:
    // it also rejects NaN, which `<=` would accept.
    #[allow(clippy::neg_cmp_op_on_partial_ord)]
    if device.is_null() || (*device).setup_done == 0 || !(frequency_hz > 0.0) {
        return E_BADPARM;
    }
    let _guard = lock();

    let mut external = [0 as c_int; CO_TERMINALS];
    let mut internal = [0 as c_int; CO_MAX_INTERNAL];
    let internal_count = device_nodes(device, &mut external, &mut internal);
    if internal_count < 0 {
        return E_UNSUPP;
    }
    let internal_count = internal_count as usize;

    (*device).ckt.CKTomega = 2.0 * CO_PI * frequency_hz;
    co_clear(device);
    let status = ffi::BSIM4v5acLoad(
        addr_of_mut!((*device).model).cast(),
        addr_of_mut!((*device).ckt),
    );
    if status != OK {
        return status;
    }

    let mut job: ffi::NOISEAN = std::mem::zeroed();
    let mut data: ffi::Ndata = std::mem::zeroed();
    job.NstartFreq = frequency_hz;
    data.freq = frequency_hz;
    data.GainSqInv = 1.0;
    (*device).noise_source_count = 0;
    let previous_job = (*device).ckt.CKTcurJob;
    (*device).ckt.CKTcurJob = addr_of_mut!(job).cast();
    ACTIVE_NOISE_DEVICE.store(device, Ordering::Release);
    let mut total_density = 0.0f64;
    let status = ffi::BSIM4v5noise(
        N_DENS,
        N_CALC,
        addr_of_mut!((*device).model).cast(),
        addr_of_mut!((*device).ckt),
        &mut data,
        &mut total_density,
    );
    ACTIVE_NOISE_DEVICE.store(null_mut(), Ordering::Release);
    (*device).ckt.CKTcurJob = previous_job;
    if status != OK {
        return status;
    }

    let mut total = [[CoComplex::ZERO; CO_TERMINALS]; CO_TERMINALS];
    let mut flicker = [[CoComplex::ZERO; CO_TERMINALS]; CO_TERMINALS];

    for source_index in 0..(*device).noise_source_count as usize {
        let source = (*device).noise_sources[source_index];
        if !source.density.is_finite() || source.density < 0.0 {
            return E_PARMVAL;
        }
        if source.density == 0.0 {
            continue;
        }

        let mut external_incidence = [CoComplex::ZERO; CO_TERMINALS];
        let mut internal_incidence = [CoComplex::ZERO; CO_MAX_INTERNAL];
        for terminal in 0..CO_TERMINALS {
            if source.node1 == external[terminal] {
                external_incidence[terminal].real += 1.0;
            }
            if source.node2 == external[terminal] {
                external_incidence[terminal].real -= 1.0;
            }
        }
        for index in 0..internal_count {
            if source.node1 == internal[index] {
                internal_incidence[index].real += 1.0;
            }
            if source.node2 == internal[index] {
                internal_incidence[index].real -= 1.0;
            }
        }

        let mut internal_voltage = [CoComplex::ZERO; CO_MAX_INTERNAL];
        if internal_count > 0 {
            let mut system = [[CoComplex::ZERO; CO_MAX_INTERNAL]; CO_MAX_INTERNAL];
            for row in 0..internal_count {
                for col in 0..internal_count {
                    system[row][col] = matrix_value(device, internal[row], internal[col]);
                }
            }
            let status = solve_complex(
                internal_count,
                &system,
                &internal_incidence,
                &mut internal_voltage,
            );
            if status != OK {
                return status;
            }
        }

        let mut reduced = [CoComplex::ZERO; CO_TERMINALS];
        for terminal in 0..CO_TERMINALS {
            reduced[terminal] = external_incidence[terminal];
            for index in 0..internal_count {
                reduced[terminal] = reduced[terminal].sub(
                    matrix_value(device, external[terminal], internal[index])
                        .mul(internal_voltage[index]),
                );
            }
        }
        for row in 0..CO_TERMINALS {
            for col in 0..CO_TERMINALS {
                let mut contribution = reduced[row].mul(reduced[col].conjugate());
                contribution.real *= source.density;
                contribution.imag *= source.density;
                total[row][col] = total[row][col].add(contribution);
                if source.index == BSIM4V5_FLNOIZ {
                    flicker[row][col] = flicker[row][col].add(contribution);
                }
            }
        }
    }

    for row in 0..CO_TERMINALS {
        for col in 0..CO_TERMINALS {
            let offset = row * CO_TERMINALS + col;
            *total_real.add(offset) = total[row][col].real;
            *total_imag.add(offset) = total[row][col].imag;
            *flicker_real.add(offset) = flicker[row][col].real;
            *flicker_imag.add(offset) = flicker[row][col].imag;
        }
    }
    OK
}

/// Batch evaluation over independent handles (host.c `co_bsim4_eval_batch`).
#[allow(clippy::too_many_arguments)]
pub unsafe fn eval_batch(
    devices: *const *mut CoBsim4,
    count: usize,
    terminals: *const f64,
    currents: *mut f64,
    conductance: *mut f64,
    charges: *mut f64,
    capacitance: *mut f64,
    statuses: *mut c_int,
) -> c_int {
    let mut first_error = OK;
    for index in 0..count {
        let status = eval_vp(
            *devices.add(index),
            terminals.add(index * CO_TERMINALS),
            currents.add(index * CO_TERMINALS),
            conductance.add(index * CO_TERMINALS * CO_TERMINALS),
            charges.add(index * CO_TERMINALS),
            capacitance.add(index * CO_TERMINALS * CO_TERMINALS),
        );
        if !statuses.is_null() {
            *statuses.add(index) = status;
        }
        if first_error == OK && status != OK {
            first_error = status;
        }
    }
    first_error
}

