    #[test]
    fn eval_point_is_finite_and_conserved() {
        unsafe {
            let device = make_device(1);
            let terminals = [0.6f64, 0.6, 0.0, 0.0]; // (drain, gate, source, bulk)
            let mut currents = [0.0f64; 4];
            let mut conductance = [0.0f64; 16];
            let mut charges = [0.0f64; 4];
            let mut capacitance = [0.0f64; 16];
            let mut op = [0.0f64; 8];
            let status = eval(
                device,
                terminals.as_ptr(),
                currents.as_mut_ptr(),
                conductance.as_mut_ptr(),
                charges.as_mut_ptr(),
                capacitance.as_mut_ptr(),
                op.as_mut_ptr(),
            );
            assert_eq!(status, OK, "eval failed");
            assert!(currents.iter().all(|v| v.is_finite()));
            assert!(conductance.iter().all(|v| v.is_finite()));
            assert!(charges.iter().all(|v| v.is_finite()));
            assert!(capacitance.iter().all(|v| v.is_finite()));

            // KCL: terminal currents sum to ~0.
            let current_sum: f64 = currents.iter().sum();
            let current_scale = currents.iter().fold(1e-18_f64, |m, v| m.max(v.abs()));
            assert!(
                current_sum.abs() <= 1e-9_f64.max(1e-8 * current_scale),
                "KCL: {current_sum:e}"
            );

            // Charge conservation: terminal charges sum to ~0.
            let charge_sum: f64 = charges.iter().sum();
            assert!(charge_sum.abs() <= 1e-25, "charge sum {charge_sum:e}");

            // Conductance columns sum to ~0 after the conservation snap.
            for col in 0..4 {
                let col_sum: f64 = (0..4).map(|row| conductance[row * 4 + col]).sum();
                assert!(col_sum.abs() <= 1e-9, "G column {col} sum {col_sum:e}");
            }
            destroy(device);
        }
    }

