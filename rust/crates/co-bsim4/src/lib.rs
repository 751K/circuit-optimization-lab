//! co-bsim4 — Rust host for the Berkeley BSIM4.5 compact model (R2).
//!
//! This crate is the Rust port of
//! `circuitopt/compact_models/bsim4/native_src/host.c`. `build.rs` compiles the
//! *unmodified* vendored Berkeley BSIM4.5 C (the same translation units
//! `native.py` builds, minus `host.c`) into a static archive and bindgens the
//! shared headers; the Rust below reimplements the host adapter layer — the
//! parameter binding, internal-node reduction, the terminal I/G/Q/C extraction,
//! and the noise combination — with the same arithmetic and operation order as
//! `host.c`, so the results match bit-for-bit (validated against the frozen
//! v1.4.0 golden corpus to 1e-13, and directly against the C `cc` backend).
//!
//! Threading: every entry that touches the vendor C takes a single crate-global
//! `Mutex`. The Berkeley sources carry ~128 file-scope mutable globals plus the
//! process-global front-end pointers wired up here, so even distinct device
//! handles must be evaluated under mutual exclusion (R2 uses a global lock; a
//! TLS-based scheme is deferred).

#![allow(clippy::missing_safety_doc)]
// This crate is a thin, faithful port of a C translation unit: nearly every
// function is `unsafe` and dereferences raw FFI pointers. Requiring an inner
// `unsafe {}` around each such op (the edition-2024 default) would bury the
// port in noise without adding signal, so we opt the whole crate out.
#![allow(unsafe_op_in_unsafe_fn)]
// The dense solves and the terminal reductions mirror host.c's explicit index
// loops verbatim so the floating-point evaluation order — and therefore the
// result bits — match the reference C. Rewriting them as iterator chains would
// obscure that correspondence, so `needless_range_loop` is opted out crate-wide.
#![allow(clippy::needless_range_loop)]

use std::ffi::CStr;
use std::os::raw::{c_char, c_int, c_long, c_void};
use std::ptr::{addr_of, addr_of_mut, null_mut};
use std::sync::atomic::{AtomicPtr, Ordering};
use std::sync::{Mutex, MutexGuard};

