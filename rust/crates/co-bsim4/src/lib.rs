// ---------------------------------------------------------------------------
// Host state layout (host.c `CoBsim4`). The embedded vendor structs come from
// bindgen so their layout matches the compiled C; the rest is host bookkeeping.
// The whole struct is only ever seen by this crate — the vendor sees pointers
// to `model` / `instance` / `ckt` — so its precise byte layout need only be
// self-consistent for the `CKTmkVolt` recovery via `offset_of!`.
// ---------------------------------------------------------------------------

#[repr(C)]
struct MatrixFrame {
    value: [[[f64; 2]; CO_MAX_NODES]; CO_MAX_NODES],
}

#[repr(C)]
#[derive(Clone, Copy)]
struct CoNoiseSource {
    index: c_int,
    node1: c_int,
    node2: c_int,
    density: f64,
}

#[repr(C)]
pub struct CoBsim4 {
    model: ffi::BSIM4v5model,
    instance: ffi::BSIM4v5instance,
    ckt: ffi::CKTcircuit,
    matrix: MatrixFrame,
    rhs: [f64; CO_MAX_NODES],
    rhs_old: [f64; CO_MAX_NODES],
    irhs: [f64; CO_MAX_NODES],
    state0: [f64; CO_STATES],
    state1: [f64; CO_STATES],
    nodes: [*mut ffi::CKTnode; CO_MAX_NODES],
    noise_sources: [CoNoiseSource; CO_MAX_NOISE_SOURCES],
    noise_source_count: c_int,
    setup_done: c_int,
}

// ---------------------------------------------------------------------------
// Crate-global vendor lock.
// ---------------------------------------------------------------------------

static VENDOR_LOCK: Mutex<()> = Mutex::new(());

#[inline]
fn lock() -> MutexGuard<'static, ()> {
    // Poisoning only signals a prior panic; the vendor scratch is reset at the
    // top of every evaluation, so recovering the guard is safe.
    VENDOR_LOCK
        .lock()
        .unwrap_or_else(|poisoned| poisoned.into_inner())
}

// ---------------------------------------------------------------------------
// Process-global front-end symbols the vendor C references.
//
// `AtomicPtr<T>` has the same in-memory representation as `*mut T`, so these
// `#[unsafe(no_mangle)]` statics present exactly the pointer-shaped symbols the vendor
// declares `extern` (`ft_curckt`, `SPfrontEnd`, `slogp`); they are populated
// once, under the lock, before any vendor call reads them.
// ---------------------------------------------------------------------------

#[unsafe(no_mangle)]
pub static ft_curckt: AtomicPtr<ffi::circ> = AtomicPtr::new(null_mut());
#[unsafe(no_mangle)]
pub static SPfrontEnd: AtomicPtr<ffi::IFfrontEnd> = AtomicPtr::new(null_mut());
#[unsafe(no_mangle)]
pub static slogp: AtomicPtr<c_void> = AtomicPtr::new(null_mut());

// Berkeley physical constants (const.h `extern double`), read by b4v5temp.c /
// b4v5ld.c. host.c defined CONSTroot2 as 1.4142135623730950488, which rounds to
// the same f64 as `SQRT_2`; CONSTvt0 is pinned to host.c's literal.
#[unsafe(no_mangle)]
pub static CONSTroot2: f64 = std::f64::consts::SQRT_2;
#[unsafe(no_mangle)]
pub static CONSTvt0: f64 = 0.025_864_19;

/// Device currently collecting noise sources (host.c `co_active_noise_device`).
/// Set/cleared under the lock around the vendor noise call; the vendor invokes
/// `CircuitOptBsim4NoiseSource` synchronously on the same thread.
static ACTIVE_NOISE_DEVICE: AtomicPtr<CoBsim4> = AtomicPtr::new(null_mut());

/// No-op error sink (host.c `co_errorf`). The vendor front end declares
/// `IFerrorf` as variadic `void (*)(int, const char*, ...)`; this callee simply
/// ignores every argument, which is ABI-safe on the supported targets (extra
/// variadic arguments live in registers/stack the callee never reads).
extern "C" fn co_errorf(_level: c_int, _fmt: *const c_char) {}

/// Wire up the process-global front end exactly once, under the lock.
unsafe fn ensure_frontend() {
    if !SPfrontEnd.load(Ordering::Acquire).is_null() {
        return;
    }
    // Front end with only the error sink populated.
    let mut frontend: Box<ffi::IFfrontEnd> = Box::new(std::mem::zeroed());
    let sink: unsafe extern "C" fn(c_int, *const c_char, ...) =
        std::mem::transmute(co_errorf as extern "C" fn(c_int, *const c_char));
    frontend.IFerrorf = Some(sink);
    SPfrontEnd.store(Box::into_raw(frontend), Ordering::Release);

    // Front-end circuit + task: `ft_curckt->ci_curTask->jobs` is walked by
    // b4v5set.c (an empty list here).
    let circ_ptr = Box::into_raw(Box::new(std::mem::zeroed::<ffi::circ>()));
    let task_ptr = Box::into_raw(Box::new(std::mem::zeroed::<ffi::TSKtask>()));
    (*task_ptr).jobs = null_mut();
    (*circ_ptr).ci_curTask = task_ptr;
    ft_curckt.store(circ_ptr, Ordering::Release);
}

