/// Allocate and initialise a device. Returns a null pointer on failure. The
/// caller owns the handle and must release it with [`destroy`].
pub unsafe fn create(polarity: c_int, temperature_k: f64) -> *mut CoBsim4 {
    let _guard = lock();
    ensure_frontend();

    let layout = std::alloc::Layout::new::<CoBsim4>();
    let device = std::alloc::alloc_zeroed(layout) as *mut CoBsim4;
    if device.is_null() {
        return null_mut();
    }

    (*device).model.BSIM4v5modName = MODEL_NAME.as_ptr() as *mut c_char;
    (*device).model.BSIM4v5instances = addr_of_mut!((*device).instance);
    (*device).instance.BSIM4v5modPtr = addr_of_mut!((*device).model);
    (*device).instance.BSIM4v5name = INSTANCE_NAME.as_ptr() as *mut c_char;
    (*device).instance.BSIM4v5dNode = 1;
    (*device).instance.BSIM4v5gNodeExt = 2;
    (*device).instance.BSIM4v5sNode = 3;
    (*device).instance.BSIM4v5bNode = 4;

    (*device).ckt.CKTmatrix = addr_of_mut!((*device).matrix).cast();
    (*device).ckt.CKTrhs = addr_of_mut!((*device).rhs) as *mut f64;
    (*device).ckt.CKTrhsOld = addr_of_mut!((*device).rhs_old) as *mut f64;
    (*device).ckt.CKTirhs = addr_of_mut!((*device).irhs) as *mut f64;
    (*device).ckt.CKTstates[0] = addr_of_mut!((*device).state0) as *mut f64;
    (*device).ckt.CKTstates[1] = addr_of_mut!((*device).state1) as *mut f64;
    (*device).ckt.CKTtemp = temperature_k;
    (*device).ckt.CKTnomTemp = 300.15;
    (*device).ckt.CKTmaxEqNum = 4;
    (*device).ckt.CKTabstol = 1.0e-12;
    (*device).ckt.CKTreltol = 1.0e-6;
    (*device).ckt.CKTvoltTol = 1.0e-6;
    (*device).ckt.CKTgmin = 1.0e-12;
    (*device).ckt.CKTbypass = 0;

    let name: &[u8] = if polarity > 0 { b"nmos" } else { b"pmos" };
    let (base, count) = model_table();
    match find_param(base, count, name) {
        Some(entry) if set_param(entry, 1.0, true, device) == OK => device,
        _ => {
            std::alloc::dealloc(device as *mut u8, layout);
            null_mut()
        }
    }
}

pub unsafe fn destroy(device: *mut CoBsim4) {
    if device.is_null() {
        return;
    }
    let _guard = lock();
    for i in 0..CO_MAX_NODES {
        let node = (*device).nodes[i];
        if !node.is_null() {
            libc::free(node.cast());
        }
    }
    // Leak fix (host.c never freed this): BSIM4v5temp allocates a
    // size-dependent parameter "knot" chain on the model (b4v5temp.c:364);
    // walk `pNext` and release every node.
    let mut knot = (*device).model.pSizeDependParamKnot;
    while !knot.is_null() {
        let next = (*knot).pNext;
        libc::free(knot.cast());
        knot = next;
    }
    std::alloc::dealloc(device as *mut u8, std::alloc::Layout::new::<CoBsim4>());
}

pub unsafe fn set_model(device: *mut CoBsim4, name: *const c_char, value: f64) -> c_int {
    if device.is_null() {
        return E_NOCHANGE;
    }
    let _guard = lock();
    if (*device).setup_done != 0 {
        return E_NOCHANGE;
    }
    let (base, count) = model_table();
    match find_param(base, count, CStr::from_ptr(name).to_bytes()) {
        Some(entry) => set_param(entry, value, true, device),
        None => E_BADPARM,
    }
}

pub unsafe fn set_instance(device: *mut CoBsim4, name: *const c_char, value: f64) -> c_int {
    if device.is_null() {
        return E_NOCHANGE;
    }
    let _guard = lock();
    if (*device).setup_done != 0 {
        return E_NOCHANGE;
    }
    let (base, count) = instance_table();
    match find_param(base, count, CStr::from_ptr(name).to_bytes()) {
        Some(entry) => set_param(entry, value, false, device),
        None => E_BADPARM,
    }
}

pub unsafe fn setup(device: *mut CoBsim4) -> c_int {
    if device.is_null() {
        return E_BADPARM;
    }
    let _guard = lock();
    let mut states: c_int = 0;
    let status = ffi::BSIM4v5setup(
        addr_of_mut!((*device).matrix).cast(),
        addr_of_mut!((*device).model).cast(),
        addr_of_mut!((*device).ckt),
        &mut states,
    );
    if status != OK {
        return status;
    }
    (*device).ckt.CKTnumStates = states;
    let status = ffi::BSIM4v5temp(
        addr_of_mut!((*device).model).cast(),
        addr_of_mut!((*device).ckt),
    );
    if status == OK {
        (*device).setup_done = 1;
    }
    status
}

pub unsafe fn node_count(device: *const CoBsim4) -> c_int {
    if device.is_null() {
        0
    } else {
        (*device).ckt.CKTmaxEqNum + 1
    }
}

// ---------------------------------------------------------------------------
// Dense linear solves (host.c `co_solve_real` / `co_solve_complex`), ported with
// identical partial-pivoting and elimination order for bit-level parity.
// ---------------------------------------------------------------------------

unsafe fn co_clear(device: *mut CoBsim4) {
    std::ptr::write_bytes(
        addr_of_mut!((*device).matrix) as *mut u8,
        0,
        std::mem::size_of::<MatrixFrame>(),
    );
    (*device).rhs = [0.0; CO_MAX_NODES];
    (*device).irhs = [0.0; CO_MAX_NODES];
}

fn solve_real(
    size: usize,
    matrix: &[[f64; CO_MAX_INTERNAL]; CO_MAX_INTERNAL],
    rhs: &[f64; CO_MAX_INTERNAL],
    solution: &mut [f64; CO_MAX_INTERNAL],
) -> c_int {
    let mut work = *matrix;
    let mut values = *rhs;

    for pivot in 0..size {
        let mut best = pivot;
        let mut magnitude = work[pivot][pivot].abs();
        for row in (pivot + 1)..size {
            let candidate = work[row][pivot].abs();
            if candidate > magnitude {
                magnitude = candidate;
                best = row;
            }
        }
        if magnitude < 1.0e-30 {
            return E_PANIC;
        }
        if best != pivot {
            for col in pivot..size {
                let temporary = work[pivot][col];
                work[pivot][col] = work[best][col];
                work[best][col] = temporary;
            }
            values.swap(pivot, best);
        }
        for row in (pivot + 1)..size {
            let factor = work[row][pivot] / work[pivot][pivot];
            work[row][pivot] = 0.0;
            for col in (pivot + 1)..size {
                // host.c `work[row][col] -= factor * work[pivot][col]`; clang
                // (-ffp-contract=on) fuses this to an FMA — match it exactly.
                work[row][col] = (-factor).mul_add(work[pivot][col], work[row][col]);
            }
            values[row] = (-factor).mul_add(values[pivot], values[row]);
        }
    }
    for row in (0..size).rev() {
        let mut value = values[row];
        for col in (row + 1)..size {
            value = (-work[row][col]).mul_add(solution[col], value);
        }
        solution[row] = value / work[row][row];
    }
    OK
}

#[derive(Clone, Copy)]
struct CoComplex {
    real: f64,
    imag: f64,
}

impl CoComplex {
    const ZERO: CoComplex = CoComplex {
        real: 0.0,
        imag: 0.0,
    };

    #[inline]
    fn sub(self, other: CoComplex) -> CoComplex {
        CoComplex {
            real: self.real - other.real,
            imag: self.imag - other.imag,
        }
    }
    #[inline]
    fn add(self, other: CoComplex) -> CoComplex {
        CoComplex {
            real: self.real + other.real,
            imag: self.imag + other.imag,
        }
    }
    #[inline]
    fn mul(self, other: CoComplex) -> CoComplex {
        CoComplex {
            real: self.real * other.real - self.imag * other.imag,
            imag: self.real * other.imag + self.imag * other.real,
        }
    }
    #[inline]
    fn conjugate(self) -> CoComplex {
        CoComplex {
            real: self.real,
            imag: -self.imag,
        }
    }
    #[inline]
    fn div(self, other: CoComplex) -> CoComplex {
        let scale = other.real * other.real + other.imag * other.imag;
        CoComplex {
            real: (self.real * other.real + self.imag * other.imag) / scale,
            imag: (self.imag * other.real - self.real * other.imag) / scale,
        }
    }
    #[inline]
    fn abs2(self) -> f64 {
        self.real * self.real + self.imag * self.imag
    }
}

fn solve_complex(
    size: usize,
    matrix: &[[CoComplex; CO_MAX_INTERNAL]; CO_MAX_INTERNAL],
    rhs: &[CoComplex; CO_MAX_INTERNAL],
    solution: &mut [CoComplex; CO_MAX_INTERNAL],
) -> c_int {
    let mut work = *matrix;
    let mut values = *rhs;

    for pivot in 0..size {
        let mut best = pivot;
        let mut magnitude = work[pivot][pivot].abs2();
        for row in (pivot + 1)..size {
            let candidate = work[row][pivot].abs2();
            if candidate > magnitude {
                magnitude = candidate;
                best = row;
            }
        }
        if magnitude < 1.0e-60 {
            return E_PANIC;
        }
        if best != pivot {
            for col in pivot..size {
                let temporary = work[pivot][col];
                work[pivot][col] = work[best][col];
                work[best][col] = temporary;
            }
            values.swap(pivot, best);
        }
        for row in (pivot + 1)..size {
            let factor = work[row][pivot].div(work[pivot][pivot]);
            work[row][pivot] = CoComplex::ZERO;
            for col in (pivot + 1)..size {
                work[row][col] = work[row][col].sub(factor.mul(work[pivot][col]));
            }
            values[row] = values[row].sub(factor.mul(values[pivot]));
        }
    }
    for row in (0..size).rev() {
        let mut value = values[row];
        for col in (row + 1)..size {
            value = value.sub(work[row][col].mul(solution[col]));
        }
        solution[row] = value.div(work[row][row]);
    }
    OK
}

#[inline]
unsafe fn matrix_value(device: *const CoBsim4, row: c_int, col: c_int) -> CoComplex {
    CoComplex {
        real: (*device).matrix.value[row as usize][col as usize][0],
        imag: (*device).matrix.value[row as usize][col as usize][1],
    }
}

unsafe fn device_nodes(
    device: *const CoBsim4,
    external: &mut [c_int; CO_TERMINALS],
    internal: &mut [c_int; CO_MAX_INTERNAL],
) -> c_int {
    external[0] = (*device).instance.BSIM4v5dNode;
    external[1] = (*device).instance.BSIM4v5gNodeExt;
    external[2] = (*device).instance.BSIM4v5sNode;
    external[3] = (*device).instance.BSIM4v5bNode;
    let candidates = [
        (*device).instance.BSIM4v5dNodePrime,
        (*device).instance.BSIM4v5gNodePrime,
        (*device).instance.BSIM4v5gNodeMid,
        (*device).instance.BSIM4v5sNodePrime,
        (*device).instance.BSIM4v5bNodePrime,
        (*device).instance.BSIM4v5dbNode,
        (*device).instance.BSIM4v5sbNode,
    ];
    let mut count: usize = 0;
    for &node in candidates.iter() {
        let is_external = external.contains(&node);
        let duplicate = internal[..count].contains(&node);
        if node > 0 && !is_external && !duplicate {
            if count >= CO_MAX_INTERNAL {
                return -1;
            }
            internal[count] = node;
            count += 1;
        }
    }
    count as c_int
}

// ---------------------------------------------------------------------------
// DC + small-signal core (host.c `co_bsim4_dc`). Requires the lock to be held.
// ---------------------------------------------------------------------------

#[allow(clippy::too_many_arguments)]
unsafe fn dc_inner(
    device: *mut CoBsim4,
    terminals: *const f64,
    currents: *mut f64,
    conductance: *mut f64,
    charges: *mut f64,
    capacitance: *mut f64,
    op: *mut f64,
) -> c_int {
    if device.is_null() || (*device).setup_done == 0 {
        return E_BADPARM;
    }
    let term = [
        *terminals.add(0),
        *terminals.add(1),
        *terminals.add(2),
        *terminals.add(3),
    ];

    let mut external = [0 as c_int; CO_TERMINALS];
    let mut internal = [0 as c_int; CO_MAX_INTERNAL];
    let internal_count = device_nodes(device, &mut external, &mut internal);
    if internal_count < 0 {
        return E_UNSUPP;
    }
    let internal_count = internal_count as usize;

    for i in 0..CO_TERMINALS {
        (*device).rhs_old[external[i] as usize] = term[i];
    }
    if internal_count >= 1 && (*device).rhs_old[internal[0] as usize] == 0.0 {
        (*device).rhs_old[internal[0] as usize] = term[0];
    }
    if internal_count >= 2 && (*device).rhs_old[internal[1] as usize] == 0.0 {
        (*device).rhs_old[internal[1] as usize] = term[2];
    }

    (*device).ckt.CKTmode = MODEDCOP | MODEINITFLOAT;
    for iteration in 0..40 {
        co_clear(device);
        let status = ffi::BSIM4v5load(
            addr_of_mut!((*device).model).cast(),
            addr_of_mut!((*device).ckt),
        );
        if status != OK {
            return status;
        }
        if internal_count == 0 {
            break;
        }

        let mut system = [[0.0f64; CO_MAX_INTERNAL]; CO_MAX_INTERNAL];
        let mut right = [0.0f64; CO_MAX_INTERNAL];
        let mut next = [0.0f64; CO_MAX_INTERNAL];
        for row in 0..internal_count {
            right[row] = (*device).rhs[internal[row] as usize];
            for j in 0..CO_TERMINALS {
                let m = (*device).matrix.value[internal[row] as usize][external[j] as usize][0];
                right[row] = (-m).mul_add(term[j], right[row]);
            }
            for col in 0..internal_count {
                system[row][col] =
                    (*device).matrix.value[internal[row] as usize][internal[col] as usize][0];
            }
        }
        let status = solve_real(internal_count, &system, &right, &mut next);
        if status != OK {
            return status;
        }
        let mut error = 0.0f64;
        for i in 0..internal_count {
            error = error.max((next[i] - (*device).rhs_old[internal[i] as usize]).abs());
            (*device).rhs_old[internal[i] as usize] = next[i];
        }
        if error < 1.0e-12 {
            break;
        }
        if iteration == 39 {
            return E_PANIC;
        }
    }

    co_clear(device);
    let status = ffi::BSIM4v5load(
        addr_of_mut!((*device).model).cast(),
        addr_of_mut!((*device).ckt),
    );
    if status != OK {
        return status;
    }

    for row in 0..CO_TERMINALS {
        let r = external[row] as usize;
        let mut residual = -(*device).rhs[r];
        for col in 0..CO_TERMINALS {
            let m = (*device).matrix.value[r][external[col] as usize][0];
            residual = m.mul_add(term[col], residual);
        }
        for col in 0..internal_count {
            let m = (*device).matrix.value[r][internal[col] as usize][0];
            residual = m.mul_add((*device).rhs_old[internal[col] as usize], residual);
        }
        *currents.add(row) = residual;
    }

    for row in 0..CO_TERMINALS {
        for col in 0..CO_TERMINALS {
            let mut reduced =
                (*device).matrix.value[external[row] as usize][external[col] as usize][0];
            if internal_count > 0 {
                let mut system = [[0.0f64; CO_MAX_INTERNAL]; CO_MAX_INTERNAL];
                let mut right = [0.0f64; CO_MAX_INTERNAL];
                let mut solution = [0.0f64; CO_MAX_INTERNAL];
                for i in 0..internal_count {
                    right[i] =
                        (*device).matrix.value[internal[i] as usize][external[col] as usize][0];
                    for j in 0..internal_count {
                        system[i][j] =
                            (*device).matrix.value[internal[i] as usize][internal[j] as usize][0];
                    }
                }
                let status = solve_real(internal_count, &system, &right, &mut solution);
                if status != OK {
                    return status;
                }
                for i in 0..internal_count {
                    let m = (*device).matrix.value[external[row] as usize][internal[i] as usize][0];
                    reduced = (-m).mul_add(solution[i], reduced);
                }
            }
            *conductance.add(row * CO_TERMINALS + col) = reduced;
        }
    }

    (*device).ckt.CKTmode = MODEDCOP | MODEINITSMSIG;
    co_clear(device);
    let status = ffi::BSIM4v5load(
        addr_of_mut!((*device).model).cast(),
        addr_of_mut!((*device).ckt),
    );
    if status != OK {
        return status;
    }
    let state_base = (*device).instance.BSIM4v5states;
    *charges.add(0) = (*device).state0[(state_base + OFF_QD) as usize];
    *charges.add(1) = (*device).state0[(state_base + OFF_QG) as usize];
    *charges.add(2) = (*device).state0[(state_base + OFF_QS) as usize];
    *charges.add(3) = (*device).state0[(state_base + OFF_QB) as usize];
    if (*device).instance.BSIM4v5rbodyMod != 0 {
        *charges.add(3) += (*device).state0[(state_base + OFF_QBD) as usize];
        *charges.add(3) += (*device).state0[(state_base + OFF_QBS) as usize];
    }
    let model_type = (*device).model.BSIM4v5type as f64;
    for terminal in 0..CO_TERMINALS {
        *charges.add(terminal) *= model_type;
    }

    (*device).ckt.CKTomega = 1.0;
    co_clear(device);
    let status = ffi::BSIM4v5acLoad(
        addr_of_mut!((*device).model).cast(),
        addr_of_mut!((*device).ckt),
    );
    if status != OK {
        return status;
    }
    for row in 0..CO_TERMINALS {
        for col in 0..CO_TERMINALS {
            let mut reduced = matrix_value(device, external[row], external[col]);
            if internal_count > 0 {
                let mut system = [[CoComplex::ZERO; CO_MAX_INTERNAL]; CO_MAX_INTERNAL];
                let mut right = [CoComplex::ZERO; CO_MAX_INTERNAL];
                let mut solution = [CoComplex::ZERO; CO_MAX_INTERNAL];
                for i in 0..internal_count {
                    right[i] = matrix_value(device, internal[i], external[col]);
                    for j in 0..internal_count {
                        system[i][j] = matrix_value(device, internal[i], internal[j]);
                    }
                }
                let status = solve_complex(internal_count, &system, &right, &mut solution);
                if status != OK {
                    return status;
                }
                for i in 0..internal_count {
                    reduced = reduced
                        .sub(matrix_value(device, external[row], internal[i]).mul(solution[i]));
                }
            }
            *capacitance.add(row * CO_TERMINALS + col) = reduced.imag;
        }
    }

    *op.add(0) = (*device).instance.BSIM4v5cd;
    *op.add(1) = (*device).instance.BSIM4v5gm;
    *op.add(2) = (*device).instance.BSIM4v5gds;
    *op.add(3) = (*device).instance.BSIM4v5gmbs;
    *op.add(4) = (*device).instance.BSIM4v5von;
    *op.add(5) = (*device).instance.BSIM4v5vdsat;
    *op.add(6) = (*device).instance.BSIM4v5ueff;
    *op.add(7) = internal_count as f64;
    OK
}

/// In-C conservation snap (host.c `co_bsim4_enforce_terminal_conservation`).
unsafe fn enforce_terminal_conservation(
    currents: *mut f64,
    conductance: *mut f64,
    charges: *mut f64,
    capacitance: *mut f64,
) -> c_int {
    let mut current_error: f64 = 0.0;
    let mut current_scale: f64 = 1.0e-18;
    let mut charge_error: f64 = 0.0;
    let mut charge_scale: f64 = 1.0e-24;
    for terminal in 0..CO_TERMINALS {
        current_error += *currents.add(terminal);
        current_scale = current_scale.max((*currents.add(terminal)).abs());
        charge_error += *charges.add(terminal);
        charge_scale = charge_scale.max((*charges.add(terminal)).abs());
    }
    if current_error.abs() > (1.0e-8 * current_scale).max(1.0e-9) {
        return E_PANIC;
    }
    if charge_error.abs() > (1.0e-8 * charge_scale).max(1.0e-18) {
        return E_PANIC;
    }
    *currents.add(CO_TERMINALS - 1) -= current_error;
    *charges.add(CO_TERMINALS - 1) -= charge_error;

    let mut conductance_scale: f64 = 1.0e-18;
    let mut capacitance_scale: f64 = 1.0e-24;
    for offset in 0..CO_TERMINALS * CO_TERMINALS {
        conductance_scale = conductance_scale.max((*conductance.add(offset)).abs());
        capacitance_scale = capacitance_scale.max((*capacitance.add(offset)).abs());
    }
    for column in 0..CO_TERMINALS {
        let mut conductance_error: f64 = 0.0;
        let mut capacitance_error: f64 = 0.0;
        for row in 0..CO_TERMINALS {
            conductance_error += *conductance.add(row * CO_TERMINALS + column);
            capacitance_error += *capacitance.add(row * CO_TERMINALS + column);
        }
        if conductance_error.abs() > (1.0e-8 * conductance_scale).max(1.0e-9) {
            return E_PANIC;
        }
        if capacitance_error.abs() > (1.0e-8 * capacitance_scale).max(1.0e-18) {
            return E_PANIC;
        }
        *conductance.add((CO_TERMINALS - 1) * CO_TERMINALS + column) -= conductance_error;
        *capacitance.add((CO_TERMINALS - 1) * CO_TERMINALS + column) -= capacitance_error;
    }
    OK
}

