// ---------------------------------------------------------------------------
// Parameter binding (host.c `co_find_param` / `co_set_param`).
// ---------------------------------------------------------------------------

unsafe fn find_param(
    base: *const ffi::IFparm,
    count: c_int,
    name: &[u8],
) -> Option<*const ffi::IFparm> {
    for i in 0..count as isize {
        let entry = base.offset(i);
        if CStr::from_ptr((*entry).keyword)
            .to_bytes()
            .eq_ignore_ascii_case(name)
        {
            return Some(entry);
        }
    }
    None
}

unsafe fn set_param(
    entry: *const ffi::IFparm,
    value: f64,
    model: bool,
    device: *mut CoBsim4,
) -> c_int {
    let mut data: ffi::IFvalue = std::mem::zeroed();
    match (*entry).dataType & IF_VARTYPES {
        v if v == IF_INTEGER || v == IF_FLAG => data.iValue = value.round() as i64 as c_int,
        v if v == IF_REAL => data.rValue = value,
        _ => return E_BADPARM,
    }
    if model {
        ffi::BSIM4v5mParam((*entry).id, &mut data, addr_of_mut!((*device).model).cast())
    } else {
        ffi::BSIM4v5param(
            (*entry).id,
            &mut data,
            addr_of_mut!((*device).instance).cast(),
            null_mut(),
        )
    }
}

#[inline]
unsafe fn model_table() -> (*const ffi::IFparm, c_int) {
    (
        addr_of!(ffi::BSIM4v5mPTable) as *const ffi::IFparm,
        addr_of!(ffi::BSIM4v5mPTSize).read(),
    )
}

#[inline]
unsafe fn instance_table() -> (*const ffi::IFparm, c_int) {
    (
        addr_of!(ffi::BSIM4v5pTable) as *const ffi::IFparm,
        addr_of!(ffi::BSIM4v5pTSize).read(),
    )
}

