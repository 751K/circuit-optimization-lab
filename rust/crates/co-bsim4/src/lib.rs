#[unsafe(no_mangle)]
pub unsafe extern "C" fn NIintegrate(
    ckt: *mut ffi::CKTcircuit,
    geq: *mut f64,
    ceq: *mut f64,
    _cap: f64,
    state: c_int,
) -> c_int {
    let s = state as usize;
    let state0 = (*ckt).CKTstates[0];
    let state1 = (*ckt).CKTstates[1];
    *geq = (*ckt).CKTag[0];
    *state0.add(s + 1) = (*ckt).CKTag[0] * *state0.add(s) + (*ckt).CKTag[1] * *state1.add(s);
    *ceq = *state0.add(s + 1);
    OK
}

#[unsafe(no_mangle)]
pub unsafe extern "C" fn NevalSrc(
    noise: *mut f64,
    ln_noise: *mut f64,
    ckt: *mut ffi::CKTcircuit,
    r#type: c_int,
    _node1: c_int,
    _node2: c_int,
    parameter: f64,
) {
    if r#type == THERMNOISE {
        *noise = 4.0 * 1.380_649e-23 * (*ckt).CKTtemp * parameter;
    } else if r#type == SHOTNOISE {
        *noise = 2.0 * 1.602_176_634e-19 * parameter.abs();
    } else if r#type == N_GAIN {
        *noise = 1.0;
    } else {
        *noise = parameter;
    }
    if !ln_noise.is_null() {
        *ln_noise = (*noise).max(1.0e-38).ln();
    }
}

/// Called by b4v5noi.c for each physical noise source (host.c hook).
#[unsafe(no_mangle)]
pub unsafe extern "C" fn CircuitOptBsim4NoiseSource(
    index: c_int,
    node1: c_int,
    node2: c_int,
    density: f64,
) {
    let device = ACTIVE_NOISE_DEVICE.load(Ordering::Acquire);
    if device.is_null() || (*device).noise_source_count >= CO_MAX_NOISE_SOURCES as c_int {
        return;
    }
    let slot = (*device).noise_source_count as usize;
    (*device).noise_source_count += 1;
    let source = &mut (*device).noise_sources[slot];
    source.index = index;
    source.node1 = node1;
    source.node2 = node2;
    source.density = density;
}

