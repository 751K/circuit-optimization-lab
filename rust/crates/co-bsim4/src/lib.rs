#[unsafe(no_mangle)]
pub unsafe extern "C" fn SMPmakeElt(
    matrix: *mut ffi::SMPmatrix,
    row: c_int,
    col: c_int,
) -> *mut f64 {
    if row < 0 || row >= CO_MAX_NODES as c_int || col < 0 || col >= CO_MAX_NODES as c_int {
        return null_mut();
    }
    let dense = matrix as *mut MatrixFrame;
    addr_of_mut!((*dense).value[row as usize][col as usize][0])
}

#[unsafe(no_mangle)]
pub unsafe extern "C" fn CKTmkVolt(
    ckt: *mut ffi::CKTcircuit,
    result: *mut *mut ffi::CKTnode,
    _basename: ffi::IFuid,
    _suffix: *mut c_char,
) -> c_int {
    let device = (ckt as *mut u8).sub(std::mem::offset_of!(CoBsim4, ckt)) as *mut CoBsim4;
    (*ckt).CKTmaxEqNum += 1;
    let number = (*ckt).CKTmaxEqNum;
    if number >= CO_MAX_NODES as c_int {
        return E_NOMEM;
    }
    let node = libc::calloc(1, std::mem::size_of::<ffi::CKTnode>()) as *mut ffi::CKTnode;
    if node.is_null() {
        return E_NOMEM;
    }
    (*node).number = number;
    (*node).type_ = SP_VOLTAGE;
    (*device).nodes[number as usize] = node;
    *result = node;
    OK
}

